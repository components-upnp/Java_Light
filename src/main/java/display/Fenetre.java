    public Fenetre( LocalService<OrdreService> order,
                    LocalService<LuminositeService> lumi){
        initComponents();
        init();
        this.orderService = order;
        this.luminositeService = lumi;
        
        this.orderService.getManager().getImplementation().getPropertyChangeSupport().addPropertyChangeListener(
                new PropertyChangeListener() {
                   @Override
                    public void propertyChange(PropertyChangeEvent evt) {
                        if(evt.getPropertyName().equals("status")){
                            actualiser_etat();
                        }
            }
        }
        );
        
        this.luminositeService.getManager().getImplementation().getPropertyChangeSupport().addPropertyChangeListener(
                new PropertyChangeListener() {
                    @Override
                    public void propertyChange(PropertyChangeEvent pce) {
                        if(pce.getPropertyName().equals("valeur") && !(etat.equals(Etat.Verouille))){
                            lumiere1.setLuminosite(
                                Integer.parseInt((String) pce.getNewValue())
                            );
                            lumiere1.actualiser();
                            slider.setValue(lumiere1.getLuminosite());
                        }
                    }
        }
        );
    }
