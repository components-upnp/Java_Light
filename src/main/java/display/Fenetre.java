    private void init(){
        etat = Etat.Deverouille;
        bt_eclaircir.setEnabled(false);
        bt_activer.setEnabled(false);
        slider.setValue(lumiere1.getLuminosite());
    } 
    
    private void actualiser_etat(){
        if(etat == Etat.Verouille){
            deverouiller();
            activer_boutons();
        } else verouiller();
    }
    
    private void verouiller(){
        etat = Etat.Verouille;
        slider.setEnabled(false);
        bt_eclaircir.setEnabled(false);
        bt_noircir.setEnabled(false);
        bt_desactiver.setEnabled(false);
        bt_activer.setEnabled(true);
    }
    
    private void deverouiller(){
        etat = Etat.Deverouille;
        slider.setEnabled(true);
        bt_eclaircir.setEnabled(true);
        bt_noircir.setEnabled(true);
        bt_desactiver.setEnabled(true);
        bt_activer.setEnabled(false);
    }
    
    private void activer_boutons(){
        if(lumiere1.getLuminosite() == 100)
            bt_eclaircir.setEnabled(false);
        else bt_eclaircir.setEnabled(true);
        if(lumiere1.getLuminosite() == 0)
            bt_noircir.setEnabled(false);
        else bt_noircir.setEnabled(true);
    }
